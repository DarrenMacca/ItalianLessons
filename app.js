/* ============================================================
   CONVERSATION INTERACTIVE EVENTS WIRE OVERLAYS
   ============================================================ */

function setupConversationEvents(convo) {
    const grid = document.getElementById("convo-options-grid");
    const nextBtn = document.getElementById("convo-next");
    const feedback = document.getElementById("convo-feedback");
    const choiceButtons = grid.querySelectorAll(".convo-choice-btn");

    choiceButtons.forEach(btn => {
        btn.addEventListener("click", () => {
            const userSelection = btn.dataset.phrase;
            const selectionMeaning = btn.dataset.meaning;
            
            // Disable all options immediately to prevent multiple selections
            choiceButtons.forEach(b => {
                b.disabled = true;
                if (b.dataset.phrase === convo.correct_phrase) {
                    b.style.border = "1px solid #4ade80";
                    b.style.background = "rgba(74, 222, 128, 0.15)";
                }
            });

            // Evaluate chosen answer path properties lookups
            if (userSelection === convo.correct_phrase) {
                btn.style.background = "#4ade80";
                btn.style.color = "#0f172a";
                
                feedback.innerHTML = `
                    <div class="quiz-correct" style="font-size: 16px; margin-bottom: 4px;">Correct Response! 🎉</div>
                    <div style="font-size: 14px; opacity: 0.85;"><strong>Meaning:</strong> ${selectionMeaning}</div>
                `;
            } else {
                btn.style.background = "#f87171";
                btn.style.color = "#0f172a";
                btn.style.border = "1px solid #f87171";
                
                feedback.innerHTML = `
                    <div class="quiz-incorrect" style="font-size: 16px; margin-bottom: 4px;">Incorrect Selection</div>
                    <div style="font-size: 14px; opacity: 0.85;">
                        You chose: <em>${userSelection}</em><br>
                        Correct answer was: <strong style="color: #4ade80;">${convo.correct_phrase}</strong> (${convo.correct_meaning})
                    </div>
                `;
            }

            // Play direct vocal response synthesizer track out to speech engine
            speakQuiz(convo.correct_phrase);

            // Reveal navigation handle panel buttons explicitly
            nextBtn.style.display = "block";

            // Save metrics out to app storage arrays variables tracks
            appState.levelStats[appState.currentLevel].conversationCompleted++;
            saveState();
            updateBadges();
            updateProgressMeters();
        });
    });

    nextBtn.addEventListener("click", () => {
        renderConversationTab();
    });
}

/* ============================================================
   RENDER VOCAB + DISRUPTOR PILLS
   ============================================================ */

function renderConversationPills(level) {
    const box = document.getElementById("convo-pill-box");

    // Collect all vocabulary from A1 → current level
    let vocab = [];
    const levels = ["A1", "A2", "B1", "B2"];
    const currentIndex = levels.indexOf(level);

    for (let i = 0; i <= currentIndex; i++) {
        const lvl = levels[i];
        if (CEFR_LEVELS[lvl]) {
            vocab = vocab.concat(CEFR_LEVELS[lvl]);
        }
    }

    // Add reduced disruptors (Mapped from French to Italian property names)
    const disruptors = DISRUPTOR_WORDS[level].map(d => ({ italian: d }));

    const finalPills = vocab.concat(disruptors);

    box.innerHTML = finalPills.map(w => `
        <button class="pill convo-pill" data-word="${w.italian}">
            ${w.italian}
        </button>
    `).join("");

    let builtSentence = [];
    const pills = document.querySelectorAll(".convo-pill");

    pills.forEach(p => {
        p.addEventListener("click", () => {
            builtSentence.push(p.dataset.word);
            document.getElementById("convo-input").value = builtSentence.join(" ");
        });
    });
}

/* ============================================================
   SCORING ENGINE
   ============================================================ */

function scoreConversationResponse(userText, expectedList) {
    const normalizedUser = userText.toLowerCase().trim();
    const wordsUser = normalizedUser.split(" ");

    let bestScore = 0;
    let bestMatch = null;

    expectedList.forEach(exp => {
        // Shifted key match target from exp.es to exp.it
        const wordsExp = exp.it.toLowerCase().split(" ");

        const matches = wordsUser.filter(w => wordsExp.includes(w)).length;
        const score = Math.round((matches / wordsExp.length) * 100);

        if (score > bestScore) {
            bestScore = score;
            bestMatch = exp;
        }
    });

    return { score: bestScore, match: bestMatch };
}

